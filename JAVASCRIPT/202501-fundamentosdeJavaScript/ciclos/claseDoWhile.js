/**
 * La sentencia Do While crea un bucle que ejecuta una sentencia especificada, hasta que la
 * condición de comprobación se evalúa como false. La condición se evalúa después de ejecutar
 * la sentencia, dando como resultado que la sentencia especificada se ejecute al menos una vez.
 */
let contador = 0;
do {
    console.log(contador);
    contador++;
} while(contador<10);
